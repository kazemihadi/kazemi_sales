package customer;

public class Customer {
	
	
	private String name;
	private float credit;
	
	
	
	public String getName() {
		return name;
	}
	public float getCredit() {
		return credit;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setCredit(float credit) {
		this.credit = credit;
	}
	
	

}
